package condiotion;

public class ForEx2 {

	public static void main(String[] args) {
		for (int i=0; i<3;i++) {
			System.out.println("Hi");
		}
		
		//1~10까지 출력
		for(int i=1;i<11;i++) {
			System.out.print(i+" ");
		}
		
		//10~1까지 출력
		for(int i=10;i>=1;i--) {
			System.out.print(i+ " ");
			
		}
	}

}
